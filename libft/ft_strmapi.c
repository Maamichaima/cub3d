#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	j;
	char	*p;

	if (!s || !f)
		return (NULL);
	i = 0;
	j = ft_strlen(s);
	p = malloc(sizeof(char) * (j + 1));
	if (!p)
		return (0);
	while (s[i] != '\0')
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
